    private void jButton_eliminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton_eliminarMouseClicked
        int x[] = jtable_datos.getSelectedRows();
        if (x.length > 0) {
            DefaultTableModel modelo = (DefaultTableModel) jtable_datos.getModel();
            for (int i = x.length - 1; i >= 0; i--) {
                modelo.removeRow(x[i]);
            }
        }
    }//GEN-LAST:event_jButton_eliminarMouseClicked

    private void jButton_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_eliminarActionPerformed
        conexion cn = new conexion("proyecto_poo");
        con = cn.conectar();
        int fila = jtable_datos.getSelectedRow();
        String valor = jtable_datos.getValueAt(fila, 0).toString();
        String query = "DELETE FROM `pacientes` WHERE cedula='" + valor + "'";

        try {
            PreparedStatement ps = con.prepareStatement(query);
            ps.executeUpdate();
            mostrar_datos("");

        } catch (SQLException ex) {
            java.util.logging.Logger.getLogger(agendamiento_citas.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "No se puedo eliminar del registro");
        }
    }//GEN-LAST:event_jButton_eliminarActionPerformed
    
