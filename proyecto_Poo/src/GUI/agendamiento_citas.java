    public void mostrar_datos(String tabla) {
        String sql = "SELECT * FROM `pacientes`" + tabla;
        Statement st;
        DefaultTableModel model = new DefaultTableModel();
        conexion cn = new conexion("proyecto_poo");
        con = cn.conectar();
        System.out.println(sql);

        model.addColumn("Cedula");
        model.addColumn("Dia");
        model.addColumn("Mes");
        model.addColumn("Año");
        model.addColumn("Hora");
        model.addColumn("Doctor");
        model.addColumn("Area Medica");
        jtable_datos.setModel(model);

        String[] datos = new String[7];

        try {

            st = con.createStatement();

            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                datos[0] = rs.getString(1);
                datos[1] = rs.getString(2);
                datos[2] = rs.getString(3);
                datos[3] = rs.getString(4);
                datos[4] = rs.getString(5);
                datos[5] = rs.getString(6);
                datos[6] = rs.getString(7);
                model.addRow(datos);
            }
            jtable_datos.setModel(model);
        } catch (SQLException ex) {
            //Logger.getLogger(agendamiento_citas.class.getName()).log(Level.SEVERE, null, ex);
            java.util.logging.Logger.getLogger(agendamiento_citas.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }
    }

